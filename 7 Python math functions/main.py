import math

pi = -3.14

x = 1
y = 2
z = 3

# round = round to the nearest integer.
print(round(pi))

# ceil/floor = round up/down
print(math.ceil(pi))
print(math.floor(pi))

# abs = absolute value of a number
print(abs(pi))

# pow = exponent
print(pow(pi, 2))

# sqrt = square root
print(math.sqrt(420))

# max/min = finding largest/lowest value of
print(max(x, y, z))
print(min(x, y, z))
